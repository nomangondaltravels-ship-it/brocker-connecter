-- NexBridge master locations + building/project directory
-- Run this in Supabase SQL Editor.
-- Safe to run multiple times.

create table if not exists public.master_locations (
  id bigint generated by default as identity primary key,
  name text not null,
  emirate text not null default 'Dubai',
  city text not null default 'Dubai',
  country text not null default 'UAE',
  aliases text[] not null default '{}'::text[],
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.master_building_projects (
  id bigint generated by default as identity primary key,
  name text not null,
  location_name text,
  emirate text not null default 'Dubai',
  developer_name text,
  aliases text[] not null default '{}'::text[],
  sort_order integer not null default 0,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists master_locations_name_unique_idx
  on public.master_locations ((lower(regexp_replace(trim(name), '\s+', ' ', 'g'))));

create unique index if not exists master_building_projects_name_location_unique_idx
  on public.master_building_projects (
    (lower(regexp_replace(trim(name), '\s+', ' ', 'g'))),
    (lower(regexp_replace(trim(coalesce(location_name, '')), '\s+', ' ', 'g')))
  );

create index if not exists master_locations_active_name_idx
  on public.master_locations (is_active, name);

create index if not exists master_building_projects_active_name_idx
  on public.master_building_projects (is_active, name);

create index if not exists master_building_projects_location_idx
  on public.master_building_projects (location_name);

create or replace function public.set_master_directory_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists master_locations_set_updated_at on public.master_locations;
create trigger master_locations_set_updated_at
before update on public.master_locations
for each row
execute function public.set_master_directory_updated_at();

drop trigger if exists master_building_projects_set_updated_at on public.master_building_projects;
create trigger master_building_projects_set_updated_at
before update on public.master_building_projects
for each row
execute function public.set_master_directory_updated_at();

alter table public.master_locations enable row level security;
alter table public.master_building_projects enable row level security;

drop policy if exists "Master locations public read" on public.master_locations;
drop policy if exists "Master locations auth read" on public.master_locations;
drop policy if exists "Master building projects public read" on public.master_building_projects;
drop policy if exists "Master building projects auth read" on public.master_building_projects;

create policy "Master locations public read"
on public.master_locations
for select
to anon
using (is_active = true);

create policy "Master locations auth read"
on public.master_locations
for select
to authenticated
using (is_active = true);

create policy "Master building projects public read"
on public.master_building_projects
for select
to anon
using (is_active = true);

create policy "Master building projects auth read"
on public.master_building_projects
for select
to authenticated
using (is_active = true);

grant select on public.master_locations to anon, authenticated;
grant select on public.master_building_projects to anon, authenticated;

