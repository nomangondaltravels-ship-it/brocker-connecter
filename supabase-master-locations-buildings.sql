update public.master_locations
set aliases = case name
  when 'JVC' then array['Jumeirah Village Circle']
  when 'JVT' then array['Jumeirah Village Triangle']
  when 'JLT' then array['Jumeirah Lakes Towers']
  when 'JBR' then array['Jumeirah Beach Residence']
  when 'DIFC' then array['Dubai International Financial Centre']
  when 'Dubai Production City' then array['IMPZ', 'International Media Production Zone']
  when 'Dubai Silicon Oasis' then array['DSO']
  when 'Dubai Land Residence Complex' then array['DLRC']
  when 'Barsha Heights' then array['Tecom', 'TECOM']
  when 'Nahda' then array['Al Nahda', 'Al Nahda Dubai', 'Al Nahda Sharjah']
  else aliases
end
where name in (
  'JVC',
  'JVT',
  'JLT',
  'JBR',
  'DIFC',
  'Dubai Production City',
  'Dubai Silicon Oasis',
  'Dubai Land Residence Complex',
  'Barsha Heights',
  'Nahda'
);

insert into public.master_locations (name, emirate, city, country, is_active)
select distinct location_name, 'Dubai', 'Dubai', 'UAE', true
from (
  select nullif(trim(location), '') as location_name from public.broker_leads
  union
  select nullif(trim(location), '') as location_name from public.broker_properties
  union
  select nullif(trim(location), '') as location_name from public.public_listings
) as source
where location_name is not null
on conflict do nothing;

insert into public.master_building_projects (name, location_name, emirate, is_active)
select distinct building_name, location_name, 'Dubai', true
from (
  select
    nullif(trim(preferred_building_project), '') as building_name,
    nullif(trim(location), '') as location_name
  from public.broker_leads
  union
  select
    nullif(trim(building_name), '') as building_name,
    nullif(trim(location), '') as location_name
  from public.broker_properties
  union
  select
    nullif(trim(building_name), '') as building_name,
    nullif(trim(location), '') as location_name
  from public.public_listings
) as source
where building_name is not null
on conflict do nothing;

with inferred_building_locations as (
  select
    lower(regexp_replace(trim(building_name), '\s+', ' ', 'g')) as building_key,
    max(location_name) as location_name
  from (
    select
      nullif(trim(preferred_building_project), '') as building_name,
      nullif(trim(location), '') as location_name
    from public.broker_leads
    union all
    select
      nullif(trim(building_name), '') as building_name,
      nullif(trim(location), '') as location_name
    from public.broker_properties
    union all
    select
      nullif(trim(building_name), '') as building_name,
      nullif(trim(location), '') as location_name
    from public.public_listings
  ) as source
  where building_name is not null
    and location_name is not null
  group by 1
)
update public.master_building_projects as target
set location_name = inferred.location_name
from inferred_building_locations as inferred
where lower(regexp_replace(trim(target.name), '\s+', ' ', 'g')) = inferred.building_key
  and coalesce(trim(target.location_name), '') = '';
